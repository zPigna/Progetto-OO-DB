package Model;

public class Trofeo {
    protected String nome;
    protected Date data;
    public Trofeo(String nome, Date data){
        this.nome = nome;
        this.data = data;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Date getData() {
        return data;
    }

    public void setData(Date data) {
        this.data = data;
    }
}
